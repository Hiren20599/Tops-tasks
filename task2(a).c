#include<stdio.h>

int main()
{
	float a, b, c;
	
	printf("enter the value of a=");
	scanf("%f", &a);
	printf("enter the value of b=");
	scanf("%f", &b);
	c= a*b;
	printf("c=a*b=%f\n", c);
	
	
	int x, y, z;
	
	printf("enter the value of x=");
	scanf("%d", &x);
	printf("enter the value of y=");
	scanf("%d", &y);
	z=x-y;
	printf("z=x-y=%d", z);
	
	return 0;
	
}
